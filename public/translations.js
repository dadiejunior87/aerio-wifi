// âš™ï¸ MOTEUR DE CHANGEMENT DE LANGUE (4 LANGUES UNIVERSELLES)
function setLang(lang) {
    localStorage.setItem('aerio_lang', lang);
    applyTranslations();
}

function applyTranslations() {
    const lang = localStorage.getItem('aerio_lang') || 'fr';
    const elements = document.querySelectorAll('[data-lang]');
    
    elements.forEach(el => {
        const key = el.getAttribute('data-lang');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    // Mise Ã  jour visuelle des boutons (FR / EN / PT / ES)
    document.querySelectorAll('.lang-btn').forEach(btn => {
        const btnLangMatch = btn.getAttribute('onclick').match(/'([^']+)'/);
        if (btnLangMatch) {
            const btnLang = btnLangMatch[1];
            btn.classList.toggle('active', btnLang === lang);
        }
    });
}
